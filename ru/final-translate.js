function translateText(text) {
    let translated = text;
    const sortedKeys = Object.keys(translationMap).sort((a, b) => b.length - a.length);
    for (const key of sortedKeys) {
        const regex = new RegExp(key, 'g');
        translated = translated.replace(regex, translationMap[key]);
    }
    translated = translated.replace(/([а-яА-Я])([A-Z])/g, '$1 $2');
    translated = translated.replace(/([A-Z])([а-яА-Я])/g, '$1 $2');
    return translated;
}

let modifiedCount = 0;
data.products.forEach(product => {
    let modified = false;
    if (/[a-zA-Z]/.test(product.name)) {
        product.name = translateText(product.name);
        modified = true;
    }
    if (/[a-zA-Z]/.test(product.description)) {
        product.description = translateText(product.description);
        modified = true;
    }
    if (/[a-zA-Z]/.test(product.category)) {
        product.category = translateText(product.category);
        modified = true;
    }
    if (modified) {
        modifiedCount++;
    }
});

const outputPath = path.join(__dirname, 'product-data.json');
fs.writeFileSync(outputPath, JSON.stringify(data, null, 2), 'utf8');
console.log(`✅ 翻译完成！共修改了 ${modifiedCount} 个产品`);
